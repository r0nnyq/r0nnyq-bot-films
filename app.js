const tabs = document.querySelectorAll('.tab-btn');
const itemList = document.getElementById('mainView');
const detailView = document.getElementById('detailView');
const detailContent = document.getElementById('detailContent');
const backBtn = document.getElementById('backBtn');
const searchInput = document.getElementById('searchInput');
const filterContainer = document.getElementById('filterContainer');

let currentCategory = 'movies';
let filters = {};

// Фильтры для каждой категории
const categoryFilters = {
    movies: ['genre', 'country', 'year'],
    books: ['genre', 'author', 'year'],
    music: ['genre', 'artist', 'year']
};

function setupTabs() {
    tabs.forEach(tab => {
        tab.addEventListener('click', () => {
            tabs.forEach(t => t.classList.remove('active'));
            tab.classList.add('active');
            currentCategory = tab.dataset.category;
            filters = {};
            searchInput.value = '';
            renderFilters();
            renderItems();
            backToList();
        });
    });
}

function renderFilters() {
    filterContainer.innerHTML = '';
    const fields = categoryFilters[currentCategory];

    fields.forEach(field => {
        const values = [...new Set(data[currentCategory].map(item => item.details[field]))].filter(Boolean).sort();

        const label = document.createElement('label');
        label.textContent = field.charAt(0).toUpperCase() + field.slice(1) + ': ';
        label.htmlFor = `filter-${field}`;

        const select = document.createElement('select');
        select.id = `filter-${field}`;
        select.dataset.field = field;

        const defaultOption = document.createElement('option');
        defaultOption.value = '';
        defaultOption.textContent = `Все`;
        select.appendChild(defaultOption);

        values.forEach(value => {
            const option = document.createElement('option');
            option.value = value;
            option.textContent = value;
            select.appendChild(option);
        });

        select.addEventListener('change', () => {
            if (select.value) {
                filters[field] = select.value;
            } else {
                delete filters[field];
            }
            renderItems();
        });

        label.appendChild(select);
        filterContainer.appendChild(label);
    });
}

function filterItems(items) {
    return items.filter(item => {
        // Поиск по названию и описанию
        const searchText = searchInput.value.trim().toLowerCase();
        const inSearch = item.title.toLowerCase().includes(searchText) || item.description.toLowerCase().includes(searchText);

        if (!inSearch) return false;

        // Фильтры по деталям
        for (const [field, value] of Object.entries(filters)) {
            if (!item.details[field] || item.details[field] !== value) return false;
        }
        return true;
    });
}

function renderItems() {
    itemList.innerHTML = '';
    const items = filterItems(data[currentCategory]);
    if (items.length === 0) {
        itemList.innerHTML = '<p style="text-align:center; color:#f0a500;">Ничего не найдено</p>';
        return;
    }

    items.forEach(item => {
        const div = document.createElement('div');
        div.className = 'item';
        div.innerHTML = `
      <img src="${item.image}" alt="${item.title}" />
      <div class="item-info">
        <div class="item-title">${item.title}</div>
        <div class="item-desc">${item.description}</div>
        <div class="item-rating">⭐ ${item.rating}</div>
      </div>
    `;
        div.addEventListener('click', () => showDetail(item));
        itemList.appendChild(div);
    });
}

function showDetail(item) {
    itemList.style.display = 'none';
    filterContainer.style.display = 'none';
    searchInput.style.display = 'none';
    tabs.forEach(t => t.style.display = 'none');

    detailView.classList.remove('hidden');

    // Формируем подробную информацию, можно добавлять любые поля
    const detailsHtml = Object.entries(item.details).map(
        ([key, val]) => `<p><strong>${key.charAt(0).toUpperCase() + key.slice(1)}:</strong> ${val}</p>`
    ).join('');

    detailContent.innerHTML = `
    <img src="${item.image}" alt="${item.title}" />
    <h2>${item.title}</h2>
    <p>${item.description}</p>
    <p><strong>⭐ Рейтинг:</strong> ${item.rating}</p>
    ${detailsHtml}
  `;
}

function backToList() {
    detailView.classList.add('hidden');
    itemList.style.display = '';
    filterContainer.style.display = '';
    searchInput.style.display = '';
    tabs.forEach(t => t.style.display = '');
}

backBtn.addEventListener('click', backToList);

searchInput.addEventListener('input', () => {
    renderItems();
});

// Инициализация
setupTabs();
renderFilters();
renderItems();
